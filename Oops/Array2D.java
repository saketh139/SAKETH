public class Array2D {
    public static void main(String args[]) {
        int marks[][] = {{1, 2, 3}, {9, 3, 4, 7}};
        for (int i = 0; i < 2; i++) {
            for (int j = 0; j < 3; j++) {
                System.out.println(" ");
                System.out.println(marks[i][j]);
               }
            }
        }

    }